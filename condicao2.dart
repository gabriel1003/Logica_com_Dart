// criando um switch case

void main() {

  String diaDaSemana = "domingo";

  switch (diaDaSemana) {
    case "domingo":
    print("é final de semana! ");
    break;
    case "segunda":
    print("Começando a semana! ");
    break;
    case "terça":
    print("quase no meio da semana! ");
    break;
    case "quarta":
    print("Chegou o meio da semana! ");
    break;
    case "quinta":
    print("Chegando o final da semana! ");
    break;
    case "sexta":
    print("Semana acabo! ");
    break;
    case "sabado":
    print("Finalmente descanso! ");
    break;
default:
print("digite um dia válido! ");
  }
}